function getSuperHero(e){
  e.preventDefault();
  fetch(`/superhero/${e.target.name.value}`)
  .then(res => res.json())
  .then(jsonRes => {
    console.log(jsonRes);
  })
}

function getHero(){
  const form = document.querySelector('.container');
  form.addEventListener('submit', (e) => getSuperHero(e));
}


document.addEventListener('DOMContentLoaded', getHero);
